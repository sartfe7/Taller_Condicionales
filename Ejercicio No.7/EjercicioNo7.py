## Programa para saber si la sumatoria de los 2 primeros numeros da como resultado el numero 3


# Input

A = int( input( " Por favor ingrese el primer numero : " ) )

B = int( input( " Por favor ingrese el segundo numero: " ) )

C = int( input( " Por favor ingrese el tercer numero : " ) )

# Processing

if A + B == C:


    print("La suma de los numeros",A,"+",B,"es igual al numero",C)

else:
    print("La suma de los numeros",A,"+",B,"no es igual al numero",C)